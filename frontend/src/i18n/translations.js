export function getTranslation(language, key, params = {}) {
  const keys = key.split(".");

  const resolvePath = (lang) => {
    let value = translations[lang];
    for (const k of keys) {
      if (value && typeof value === "object" && k in value) {
        value = value[k];
      } else {
        return undefined;
      }
    }
    return value;
  };

  let result = resolvePath(language);
  if (result == null && language !== "ko") {
    result = resolvePath("ko");
  }
  if (result == null) {
    result = key;
  }

  // Support interpolation like {{variable}}
  if (typeof result === "string" && params) {
    Object.keys(params).forEach((param) => {
      const regex = new RegExp(`{{${param}}}`, "g");
      result = result.replace(regex, params[param]);
    });
  }

  return result;
}
